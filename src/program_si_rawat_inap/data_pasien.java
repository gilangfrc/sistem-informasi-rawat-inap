    public void autonumber(){
        String nokd = "KP000";
        int i = 0;
        Connection con = null;
        Statement st = null;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rawat_inap","root","");
            st = con.createStatement();
            String sql = "Select kd_pasien from db_pasien";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                nokd = rs.getString("kd_pasien");
            }
            nokd = nokd.substring(2);
            i = Integer.parseInt(nokd)+1;
            
            nokd = "00"+i;
            nokd = "KP"+nokd.substring(nokd.length()-3);
            kd.setText(nokd);

        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),null,JOptionPane.ERROR_MESSAGE);
        }
    }
    
