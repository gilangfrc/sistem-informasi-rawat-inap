        String tl = tabel.getValueAt(baris, 3).toString();
        String tg = tabel.getValueAt(baris, 4).toString();
        String jkk = tabel.getValueAt(baris, 5).toString();
        String al = tabel.getValueAt(baris, 6).toString();
        String gol = tabel.getValueAt(baris, 7).toString();
