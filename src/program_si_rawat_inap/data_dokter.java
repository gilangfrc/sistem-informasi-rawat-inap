    public void delete(){
        Connection con = null;
        Statement st = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rawat_inap","root","");
            st = con.createStatement();
            String sql = "delete from db_dokter where kd_dokter=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, kd.getText());
            pst.execute();
            autonumber();
            tampil();
            bersih();
            JOptionPane.showMessageDialog(null, "Berhasil dihapus");
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),null,JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void pilihtabel(){
        ubah.setEnabled(true);
        buang.setEnabled(true);
        tambah.setEnabled(false);
        int baris = tabel.getSelectedRow();
        String kdd = tabel.getValueAt(baris, 1).toString();
        String nd = tabel.getValueAt(baris, 2).toString();
        String spes = tabel.getValueAt(baris, 3).toString();
        String alam = tabel.getValueAt(baris, 4).toString();
        kd.setText(kdd);
        nm.setText(nd);
        spesial.setText(spes);
        alamat.setText(alam);
    }
    
    public void bersih(){
        nm.requestFocus();
        ubah.setEnabled(false);
        buang.setEnabled(false);
        tambah.setEnabled(true);
        nm.setText("");
        spesial.setText("");
        alamat.setText("");
    }
    
    public void autonumber(){
        String nokd = "KD000";
        int i = 0;
        Connection con = null;
        Statement st = null;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rawat_inap","root","");
            st = con.createStatement();
            String sql = "Select kd_dokter from db_dokter";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                nokd = rs.getString("kd_dokter");
            }
            nokd = nokd.substring(2);
            i = Integer.parseInt(nokd)+1;
            
            nokd = "00"+i;
            nokd = "KD"+nokd.substring(nokd.length()-3);
            kd.setText(nokd);

        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),null,JOptionPane.ERROR_MESSAGE);
        }
    }
    
