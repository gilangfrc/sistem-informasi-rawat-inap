    public DefaultTableModel tabmod;
    
    public void tampil(){
        Object[] baris = {"No", "Kode Dokter", "Nama Dokter", "Spesialis", "Alamat"};
        tabmod = new DefaultTableModel(null, baris);
        tabel.setModel(tabmod);
        Connection con = null;
        Statement st = null;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rawat_inap","root","");
            st = con.createStatement();
            String sql = "Select * from db_dokter";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                String id = rs.getString("id");
                String kd_dokter = rs.getString("kd_dokter");
                String nm_dokter = rs.getString("nm_dokter");
                String spesialis = rs.getString("spesialis");
                String alamat = rs.getString("alamat");
                String[] data = {id, kd_dokter, nm_dokter, spesialis, alamat};
                tabmod.addRow(data);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Koneksi gagal\n"+e.getMessage(),null,JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void tambah(){
        String a,b,c,d,smpn;
        a = kd.getText();
        b = nm.getText();
        c = spesial.getText();
        d = alamat.getText();
               
        Connection con = null;
        Statement st = null;
        
        try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rawat_inap","root","");
                st = con.createStatement();
                //simpan = "INSERT INTO akun(nm, usrnm, pass) VALUES ('"+nma+"','"+usrnme+"','"+pw+"')";
                smpn = "INSERT INTO db_dokter(kd_dokter, nm_dokter, spesialis, alamat) VALUES ('"+a+"','Dr."+b+"','"+c+"','"+d+"')";
                st.executeUpdate(smpn);
                tampil();
                JOptionPane.showMessageDialog(null, "Berhasil ditambah");
                nm.requestFocus();
                autonumber();
                bersih();
            } 
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),null,JOptionPane.ERROR_MESSAGE);
        }
    }
    
